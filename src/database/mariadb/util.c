// strlen
#include <string.h>

#include "common.h"

void st_database_mariadb_util_prepare_string(MYSQL_BIND * bind, const char * string) {
	if (string != NULL) {
		bind->buffer_type = MYSQL_TYPE_STRING;
		bind->buffer = (char *) string;
		bind->buffer_length = strlen(string);
	} else {
		bind->buffer_type = MYSQL_TYPE_NULL;
	}
}

