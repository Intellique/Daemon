// free
#include <stdlib.h>

#include <libstone/library/vtl.h>

void st_vtl_config_free(struct st_vtl_config * config, unsigned int nb_config) {
	unsigned int i;
	for (i = 0; i < nb_config; i++) {
		struct st_vtl_config * cfg = config + i;
		free(cfg->path);
		free(cfg->prefix);
	}
	free(config);
}

