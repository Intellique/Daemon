#include <glob.h>


#include <stdio.h>

#include "common.h"

void sa_changer_setup() {
	glob_t gl;
	gl.gl_offs = 0;
	glob("/sys/class/scsi_device/*/device/scsi_changer/", GLOB_DOOFFS, 0, &gl);

	unsigned int i;
	for (i = 0; i < gl.gl_pathc; i++)
		printf("found: %s\n", gl.gl_pathv[i]);
}

